def fileNameToResponse(fileNames):
  start = time.time()
  print("üöÄ PROCESSING(4): fileName to Response")
  
  response = []
  for fileName in fileNames:
    # filename -> "1-123-1"
    floor = {}
    floor['floor_name'] = fileName
    floor['floor_src'] = FLOOR_PLAN_PATH + fileName + PNG_FILE_EXTENSION
    floor['bubblemap_kor_src'] = BUBBLE_MAP_PATH_KOR + fileName + JPG_FILE_EXTENSION
    floor['bubblemap_eng_src'] = BUBBLE_MAP_PATH_ENG + fileName + JPG_FILE_EXTENSION
    
    levels = []
    higherlevels = findHigherLevels(fileName)
    for higherlevel in higherlevels:
      upperfloor = {}
      upperfloor['floor_name'] = higherlevel
      upperfloor['floor_src'] = FLOOR_PLAN_PATH + higherlevel + PNG_FILE_EXTENSION
      levels.append(upperfloor)
    
    floor["levels"] = levels
    response.append(floor)
    
  print(f"‚è∞ filename_to_response.py: {time.time() - start}")
  return response


